package com.alevel.deliverit.customers;

/**
 * The {@code Name} class wraps a value of {@code String} name
 *
 * @author Vadym Mitin
 */
public final class Name {
    private final String name;

    public Name(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
